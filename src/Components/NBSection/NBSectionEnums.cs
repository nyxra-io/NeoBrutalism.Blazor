namespace NeoBrutalism.Blazor;

/// <summary>Background style of an <see cref="NBSection"/>.</summary>
public enum NBSectionBackground
{
    /// <summary>Default page background.</summary>
    Default,
    /// <summary>Secondary/alternating background.</summary>
    Secondary
}

/// <summary>Text alignment for title and subtitle of an <see cref="NBSection"/>.</summary>
public enum NBSectionAlign
{
    /// <summary>Center-aligned (default).</summary>
    Center,
    /// <summary>Left-aligned.</summary>
    Left,
    /// <summary>Right-aligned.</summary>
    Right
}
